  function normalizeLang(lang) {
    lang = (lang || '').toLowerCase().trim();
    if (lang.startsWith('zh-cn') || lang === 'zh' || lang === 'zh-hans' || lang.startsWith('zh-sg')) return 'zh-CN';
    if (lang.startsWith('zh-tw') || lang.startsWith('zh-hk') || lang.startsWith('zh-mo') || lang === 'zh-hant') return 'zh-TW';
    if (lang.startsWith('en')) return 'en';
    return '';
  }

  function detectLang() {
    const params = new URLSearchParams(location.search);
    const langParam = normalizeLang(params.get('lang'));
    if (langParam) return langParam;
    try {
      const stored = normalizeLang(localStorage.getItem(STORAGE_KEY));
      if (stored) return stored;
    } catch(e) {}
    const m = document.cookie.match(/aiops_lang=([^;]+)/);
    if (m) { const cl = normalizeLang(m[1]); if (cl) return cl; }
    for (const bl of navigator.languages || [navigator.language]) {
      const nl = normalizeLang(bl); if (nl) return nl;
    }
    return DEFAULT_LANG;
  }

  let currentLang = detectLang();

  function t(key, fallback) {
    const dict = I18N[currentLang] || I18N[DEFAULT_LANG];
    return dict[key] || I18N[DEFAULT_LANG][key] || fallback || key;
  }

  function applyTranslations(root) {
    root = root || document;
    root.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translated = t(key);
      if (translated && translated !== key) el.textContent = translated;
    });
    root.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      const translated = t(key);
      if (translated && translated !== key) el.title = translated;
    });
    root.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const translated = t(key);
      if (translated && translated !== key) el.placeholder = translated;
    });
    document.documentElement.lang = currentLang;
  }

  function setLang(lang) {
    const normalized = normalizeLang(lang);
    if (!normalized) return;
    currentLang = normalized;
    try { localStorage.setItem(STORAGE_KEY, normalized); } catch(e) {}
    document.cookie = 'aiops_lang=' + normalized + ';path=/;max-age=31536000';
    applyTranslations();
    window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: normalized } }));
  }

  function getLang() { return currentLang; }

  function injectLangSwitcher() {
    if (document.getElementById('lang-switcher')) return;
    const switcher = document.createElement('div');
    switcher.id = 'lang-switcher';
    switcher.style.cssText = 'position:fixed;top:12px;right:16px;z-index:9999;display:flex;gap:2px;background:rgba(128,128,128,.15);border-radius:6px;padding:2px';
    [{code:'zh-CN',label:'简中'},{code:'zh-TW',label:'繁中'},{code:'en',label:'EN'}].forEach(l => {
      const btn = document.createElement('button');
      btn.textContent = l.label;
      btn.dataset.lang = l.code;
      btn.style.cssText = 'border:none;background:transparent;color:inherit;padding:4px 8px;cursor:pointer;font-size:12px;border-radius:4px';
      btn.addEventListener('click', () => setLang(l.code));
      switcher.appendChild(btn);
    });
    document.body.appendChild(switcher);
    updateSwitcherActive();
  }

  function updateSwitcherActive() {
    document.querySelectorAll('#lang-switcher button').forEach(btn => {
      const isActive = btn.dataset.lang === currentLang;
      btn.style.fontWeight = isActive ? '700' : '400';
      btn.style.opacity = isActive ? '1' : '0.6';
    });
  }

  window.I18N = { t, applyTranslations, setLang, getLang, supported: SUPPORTED_LANGS,
    init() {
      injectLangSwitcher();
      applyTranslations();
      const observer = new MutationObserver(() => { applyTranslations(); updateSwitcherActive(); });
      observer.observe(document.body, { childList: true, subtree: true });
    }
  };

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => window.I18N.init());
  } else { window.I18N.init(); }
